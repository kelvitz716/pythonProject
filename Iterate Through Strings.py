#while loop
string = 'The big brown fox'
i = 0
while i < len(string):
    print(string[i])
    i += 1
print('\n')

#for loop
string = 'The big brown fox'
for i in range(0, len(string)):
    print(string[i])
print('\n')

#iterate through the characters
string = 'The big brown fox'
for char in string:
    print(char)
print('\n')